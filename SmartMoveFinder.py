CHECKMATE = 1000
STALEMATE = 0
MAX_DEPTH = 3
nextMove = None


def findRandomMoves(validMoves):
    """Find a random move from valid moves"""
    return validMoves[random.randint(0, len(validMoves) - 1)]


def findBestMoveMinMax(gs, validMoves, returnQueue):
    """Find the best move using MinMax algorithm"""
    global nextMove
    nextMove = None
    findMoveNegaMaxAlphaBeta(
        gs, validMoves, MAX_DEPTH, -CHECKMATE, CHECKMATE, 1 if gs.whiteToMove else -1
    )
    returnQueue.put(nextMove)


def findMoveNegaMaxAlphaBeta(gs, validMoves, depth, alpha, beta, turnMultiplier):
    """NegaMax algorithm with alpha-beta pruning"""
    global nextMove
    if depth == 0:
        return turnMultiplier * scoreBoard(gs)

    maxScore = -CHECKMATE
    for move in validMoves:
        gs.makeMove(move)
        nextMoves = gs.getValidMoves()
        score = -findMoveNegaMaxAlphaBeta(
            gs, nextMoves, depth - 1, -beta, -alpha, -turnMultiplier
        )
        if score > maxScore:
            maxScore = score
            if depth == MAX_DEPTH:
                nextMove = move
        gs.undoMove()
        if maxScore > alpha:
            alpha = maxScore
        if alpha >= beta:
            break
    return maxScore


def scoreBoard(gs):
    """
    Score the board position
    Positive score is good for white, negative for black
    """
    if gs.checkmate:
        if gs.whiteToMove:
            return -CHECKMATE
        else:
            return CHECKMATE
    elif gs.stalemate:
        return STALEMATE

    score = 0
    for row in range(len(gs.board)):
        for col in range(len(gs.board[row])):
            square = gs.board[row][col]
            if square != "--":
                pps = 0
                fac = 0.1
                color = square[0]
                piece = square[1]
                if piece != "K":
                    pps += (
                            piecePositionScores[piece if piece != "p" else square][row][col]
                            * fac
                    )
                if color == "w":
                    score += pieceScore[piece] + pps
                elif color == "b":
                    score -= pieceScore[piece] + pps
    return score